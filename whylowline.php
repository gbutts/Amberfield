<h1>Why Lowlines?</h1>

      <p>As you look around Australian Lowline marketing material, you will see plenty of
      reasons why Lowlines are the best cattle in Australia! Funnily enough, if you look at
      other breeds' web sites or promotional material, you will see similar claims and
      advantages.</p>

	    <img src="images/steers2.png" alt="Latest steers" title="Latest steers" width="500" height="269" align=
        "right"  hspace="-10" />

      <p>When we were looking at the breed over about five years with thoughts of commencing a
      stud, we read all this stuff, absorbed it and realised it could not all be true.</p>

      <p>Listed below are some of the more realistic claims, which we broadly believe to be
      true. As you can see, they can be broken down into two main areas: advantages for the
      small holder and advantages for the market.</p>

  <h2>Advantages for the Small Farmer</h2>

      <p>We dislike the label: hobby farmer. Sure there are plenty of so called primary
      producers who are not necessarily financially viable in their own right or have years or
      generations of experience to fall back on; but what they do is not just a 'hobby'.</p>

      <p>We are certainly low volume producers, but we are also committed to maintaining
      the breed and ultimately selling quality beef products just like our larger colleagues out
      west.</p>

      <p>So what are the advantages of Australian Lowline Cattle for a low volume producer?</p>

    <h3>Stocking Rates</h3>

      <p>Lowlines are marketed as being ideal for small acre properties. This is because Lowline
      cattle are about 60% of the size of their Angus ancestors and more of them are able
      to be run on any given acre. Some producers say you can run 10 Lowlines were another beef
      producer would normally stock 6 Angus cows. We are not convinced this exact ratio is
      necessarily viable, but there is no doubt that, due to their lesser body mass, and
      smaller, less demanding calves, we are running more Lowlines per acre than the equivalent
      in Blondes or Herefords (which we have also bred in the past).</p>

    <h3>Quicker Maturity</h3>

      <p>Lowlines heifers can be joined with Lowline bulls at 14 months. We have several heifers that
      calved before their second birthday. Contrast this with our Blonde d'Acquitaines and Herefords
      (when we had them), who did not become pregnant until after their second birthday.</p>

	    <img src="images/12weaners2.png" alt="Weaners" title="Weaners" width="500" height="340" align=
        "right"  hspace="-10" />

    <h3>Shorter Gestation</h3>

      <p>The Lowline gestation period at approximately 273 days is about a week shorter than
      other British breeds. This shorter period is a major advantage for dairy farmers. Using
      Lowline bulls they can get the pregnancy over and get on with milking about a week earlier
      than when they use dairy bulls. An extra week's milk over a herd of 100 cows is a
      substantial economic benefit. For the beef producer, this works out to being one more calf
      over the lifetime of a good cow than most other breeds. For small holders especially, the
      management advantage of being able to predictably plan on one calf per year at the same
      time each year should not be underestimated.</p>

    <h3>Docility</h3>

      <p>Lowlines are far less intimidating and much easier to handle than larger breeds. We had
      not been in close proximity to any beef cattle before we took delivery of our first
      Lowline heifers. We learned quickly to tread carefully and have had our share of
      incidents, but it is certainly true that their small size, docile nature and natural
      absence of horns make Lowline cattle among the easiest of all cattle breeds to handle.</p>

      <p>Provided they are treated with respect, Lowlines are particularly useful to help
      children to learn and practice cattle handling techniques. We are now involved with several 
      local schools and have loaned and donated several animals. In these
      days of protective OH&amp;S legislation, we see particular advantages in using Lowlines to
      learn the basics before agricultural students progress onto larger cattle.</p>

    <h3>Lighter Infrastructure</h3>

      <p>As with any breed of cattle, fences need to be secure, but the smaller size and docile
      nature of Lowlines also enables the use of lighter and smaller fences, yards and handling
      equipment. We have found that the use of electric fencing for new fences and to upgrade
      older fences to be satisfactory. Don't be misled, Lowlines will still 'fit' into full
      sized yards if that's what you have.</p>

    <h3>Husbandry</h3>

      <p>Lowline cattle have been developed from Angus cattle. In all aspects of their
      management and husbandry they are similar to this and other British breeds. Veterinarians
      and other suppliers are familiar with the requirements and behaviour of these cattle.
      Apart from routine maintenance, such as vaccination programs, drenching and fly and tick
      control, Lowlines require a minimum of care.</p>

	    <img src="images/paddock1.png" alt="Breeders" title="Breeders" width="500" height="375" align=
        "left"  hspace="-10" />

    <h3>Easy Start</h3>

      <p>Lowlines are an excellent starting point for entry into the beef industry, be it full
      time or as a supplementary interest. In our case, we have bred 'full size' cattle as well
      as Lowlines. We were unlikely to have considered full sized beasts as an option in the
      first instance.</p>

      <p>There are Lowline cattle promotion groups in each State with amazingly helpful
      people to assist newcomers to learn more about cattle care and husbandry. We were amazed
      at the amount of free, genuine care and assistance that we were given
      to learn about the industry. Experienced breeders hold open days, assist with the showing
      of cattle and promote the breed both in Australia and internationally. It is a valuable
      community in the real sense of the word.</p>

  <h2>Marketing Advantages</h2>

      <p>Each breed will emphasise their best features against the competition. In this arena,
      it should be remembered that Lowlines have evolved from Angus cattle. The Angus breed has
      numerous advantages in the quality (tenderness and marbling) of the meat and broad
      acceptance in the market by butchers and consumers alike.</p>

    <h3>High Carcase Yields</h3>

      <p>Put simply, the part that Lowlines lost during the selective breeding program at Trangie
      was most of their legs! In most beef cattle, legs are waste. There is very little usable
      beef and lots of heavy bones. With short legs, the percentage increase of valuable dressed
      weight over the live weight is significant. We've heard of yields as high as 70%, however
      65% is more likely. Very high carcase yields are common for Lowlines and their crosses.
      The increasing popularity and success of Lowline crosses in carcase competitions is
      testament to this.</p>

