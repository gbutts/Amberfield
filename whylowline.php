    <h3>Smaller Cuts</h3>

      <p>Lowline beef, as a traditional beef, appeals to the butcher, chef and consumer alike.
      It exhibits 'normal' beef presentation and marbling, with the traditional cuts of meat
      being smaller in size than other breeds. These tender, smaller cuts have a special appeal
      in the restaurant trade.</p>

	    <img src="images/calf1.png" alt="2 month calf" title="2 month calf" width="500" height="369" align=
        "right"  hspace="-10" />

    <h3>Minimisers</h3>

      <p>Lowline bulls are becoming increasing popular with dairy breeders, because of the
      shorter gestation period, and with beef breeders who see the benefits of calving ease,
      especially for first time heifers. The use of Lowline semen in the dairy industry for
      first calves permits earlier joining and safer calving as well as yielding vigorous beefy
      calves for feed-lotters.</p>

    <h3>More Grass into Beef</h3>

      <p>When higher percentage yields per carcase are combined with early maturity, higher
      stocking rates and shorter gestation, the potential of a well managed Lowline herd can be
      easily seen. It all adds up to turning a given amount of grass into the maximum amount of
      beef.</p>

  <h2>Where to from here?</h2>

      <p>Lowlines themselves are obviously not going to take the export beef market by storm as
      many other breeds are far more adapted to the Australian climate and provide the necessary
      economies of scale. The advantage of the Lowline breed is to add value as a first cross,
      flourishing with the benefits of hybrid vigour.</p>

      <p>Areas where small holders can benefit from Lowline stock are in the production of bulls
      for the dairy industry and for unjoined heifers; providing small, tender cuts to boutique
      restaurant markets and dedicated stud production. Potential 'sea changers' should not
      overlook the possibility of making the lifestyle decision to raise Lowlines with the
      benefit of another income; a choice which provides a great quality of life.</p>

